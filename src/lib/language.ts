export type Language = keyof typeof languages
export type TranslationKey = keyof typeof languages.en

export function getTranslation(key: TranslationKey, language: Language = 'en'): string {
  return languages[language]?.[key] || languages.en[key] || key
}

export function getCurrentLanguage(): Language {
  if (typeof window === 'undefined') return 'en'
  return (localStorage.getItem('preferred-language') as Language) || 'en'
}
